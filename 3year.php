                In conclusion, this outreach program has a lot of realization, one of that is I should strive more for my study, I should always listen to the people who has a problem because that’s the only thing I can comfort them, I should know my limitations, I should not give up and I should not allow my problems to defeat me because problems is a chance for me to do my best. Truly being a paulinian make’s a difference globally and I’m proud of it.</h6>
