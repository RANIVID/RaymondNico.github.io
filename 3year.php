                Trip to a joyful pain is an experience where you can share yourselves, where you can meet strange people, know more about your friends and personalities. The one thing that is important in this world is LISTENING every person in this world has its own sorrow, problems and a fresh wounds that change their lives because of their imperfections. Every human being has its responsibility to listen to their neighbors to know what happened to them and how did they solve their problems because that is the greatest lesson and achievement you’ve ever receive and a true paulinian should be.

